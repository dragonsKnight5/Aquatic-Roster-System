    private void ltsListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ltsListValueChanged
       try
       {
        lts selectedShift = (lts)ltsList.getSelectedValue();
        startTimeLabel.setText((selectedShift.getStartTime()).toString());
        endTimeLabel.setText((selectedShift.getEndTime()).toString());
        staffLabel.setText(selectedShift.getStaff());
        
        SimpleDateFormat myDateFormat = new SimpleDateFormat("dd/MM/YYYY");
        String formatedDate = myDateFormat.format(selectedShift.getStartDate());
        startDateLabel.setText(formatedDate);
        formatedDate = myDateFormat.format(selectedShift.getEndDate());
        endDateLabel.setText(formatedDate);
        dayCombo.setSelectedItem("All");
       }
       catch (Exception ex)
       {
           System.out.println(ex);
       }
        
    }//GEN-LAST:event_ltsListValueChanged

    private void firstLoad ()
    {
        loadData("All");
        dayCombo.setSelectedItem("All");
    }
    
    private void loadData(String day)
{
    ltsShifts.clear();
    ltsList.removeAll();
    String command;
    ResultSet returned;
    ltsList.removeAll();
    if (day.equalsIgnoreCase("All"))
    {
        if (parent.isSupervisor())
        {
            command = "select * from LTS_Shift";
        }
        else
        {
            command = "select * from LTS_Shift where staff = \"" + parent.getUser() + "\"";
        }
    }
    else
    {
        if (parent.isSupervisor())
        {
            command = "select * from LTS_Shift where shift_day = \"" + day + "\"";
        }
        else
        {
            command = "select * from LTS_Shift where shift_day = \"" + day + "\" and staff = \"" + parent.getUser() + "\"";
        }
    }
    try
    {
        returned = connection.lookup(command);
        while(returned.next())
        {
            lts ltsShift = new lts(returned.getInt("ID"), returned.getString("staff"), returned.getString("shift_day"), returned.getTime("start_time"), returned.getTime("end_time"), returned.getDate("start_date"), returned.getDate("end_date"));
            ltsShifts.add(ltsShift);
        }
        ltsList.setListData(ltsShifts.toArray());
        dayCombo.setSelectedItem("All");
        if(!ltsShifts.isEmpty())
        {
            ltsList.setSelectedIndex(0);
        }
    }
    catch (SQLException ex)
    {
        JOptionPane.showMessageDialog(parent, ex);
    }
}
    
