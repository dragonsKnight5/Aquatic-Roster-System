    private void lifeguardList (String date)
    {
        // Lifeguard list setup
        String command = null;
        if (date.equalsIgnoreCase("All"))
        {
            command = "select * from lifeguard";
        }
        
        ResultSet result = connection.lookup(command);
        try {
            result = connection.lookup(command);
            while (result.next()) {
                lifeguards tempGuard = new lifeguards(result.getInt("ID"), result.getDate("shift_Date"), result.getTime("start_time"), result.getTime("end_time"), result.getString("location"), result.getString("staff1"), result.getString("staff2"), result.getString("staff3"), result.getString("onCall"));
                guardShifts.add(tempGuard);
            }
            lifeguardList.setListData(guardShifts.toArray());
        } catch (SQLException ex) {
            System.out.println("lts load data\n" + ex);
        }
    }
    
    private void ltsList ()
    {
        // LTS list setup
        String command = "select * from LTS_Shift";
        ResultSet result = connection.lookup(command);
        try {
            while (result.next()) {
                lts ltsShift = new lts(result.getInt("ID"), result.getString("staff"), result.getString("shift_day"), result.getString("location"), result.getTime("start_time"), result.getTime("end_time"), result.getDate("start_date"), result.getDate("end_date"));
                ltsShifts.add(ltsShift);
            }
            ltsList.setListData(ltsShifts.toArray());
        } catch (SQLException ex) {
            System.out.println("lts load data\n" + ex);
        }
    }
    
