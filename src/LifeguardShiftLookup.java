private void loadData()
{
    ResultSet returned;
    String command = null;
    if (parent.isSupervisor())
    {
        if (allRadioButton.isSelected())
        {
            command = "select * from lifeguard";
        }
        else if (nameRadioButton.isSelected())
        {
            command = "select * from lifeguard where staff1 = \'" + nameTxtFld.getText() + "\' or onCall = \'" + nameTxtFld.getText() + "\'";
        }
        else
        {
            command = "select * from lifeguard where shift_date = \'" + comFunc.dateSwitch((String)dateSearchCombo.getSelectedItem()) + "\'";
        }
    }
    else
    {
        command = "select * from lifeguard where staff1 = \'" + parent.getUser() + "\' or onCall = \'" + nameTxtFld.getText() + "\'";
    }
    try
    {
        guardShifts.clear();
        System.out.println(command);
        returned = connection.lookup(command);
        while(returned.next())
        {
            lifeguards tempGuard = new lifeguards(returned.getInt("ID"), returned.getDate("shift_Date"), returned.getTime("start_time"), returned.getTime("end_time"), returned.getString("location"), returned.getString("staff1"), returned.getString("onCall"));
            guardShifts.add(tempGuard);
        }
        shiftList.setListData(guardShifts.toArray());
        if (!guardShifts.isEmpty())
        {
            shiftList.setSelectedIndex(0);
        }
    }
    catch (SQLException ex)
    {
        JOptionPane.showMessageDialog(parent, ex);
    }
    
    LocalDate tempDate = LocalDate.now();
    int count = 12;
    
    do
    {
        dateSearchCombo.addItem(comFunc.minusDaysFormated(tempDate, count));
        count --;
    } while(count != 0);
    
    do 
    {
        dateSearchCombo.addItem(comFunc.plusDaysFormated(tempDate, count));
        count++;
    } while(count != 50);
}

